Vector3 Normalize(const Vector3* original) {
	float dot = Dot(original, original);
	Vector3 result = *original;
	if (dot != 0.0f) {
		float length = sqrtf(dot);
		result.x /= length;
		result.y /= length;
	}
	return result;
}

